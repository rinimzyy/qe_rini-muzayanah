public class Kalkulator {
    void add(float angka1, float angka2) {
        System.out.print("Penjumlahan: ");
        System.out.print(angka1+angka2);
        System.out.println();
    }

    void substract(float angka1, float angka2) {
        System.out.print("Pengurangan: ");
        System.out.print(angka1-angka2);
        System.out.println();
    }

    void multiply(float angka1, float angka2) {
        System.out.print("Perkalian: ");
        System.out.print(angka1*angka2);
        System.out.println();
    }

    void divide(float angka1, float angka2) {
        System.out.print("Pembagian: ");
        System.out.print(angka1/angka2);
    }
}
